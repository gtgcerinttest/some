using Microsoft.IdentityModel.Tokens;
using MudHutAPI.Services.Interfaces;
using System.IdentityModel.Tokens.Jwt;
using System.Text;

namespace MudHutAPI.MiddleWare
{
    public class JwtMiddleware
    {

        private readonly RequestDelegate _next;
        private readonly ApiSettings _apiSettings;

        public JwtMiddleware(RequestDelegate next, ApiSettings apiSettings)
        {
            _next = next;
            _apiSettings = apiSettings;
        }

        public async Task Invoke(HttpContext context, IUserService userService)
        {
            var token = context.Request.Headers["Authorization"].FirstOrDefault()?.Split(" ").Last();

            if (token != null)
                await attachUserToContext(context, userService, token);

            await _next(context);
        }

        private async Task attachUserToContext(HttpContext context, IUserService userService, string token)
        {
            try
            {
                var tokenHandler = new JwtSecurityTokenHandler();
                var key = Encoding.ASCII.GetBytes(_apiSettings.Secret);
                //TODO: Review TokenValidationParameters
                tokenHandler.ValidateToken(token, new TokenValidationParameters
                {
                    ValidateIssuerSigningKey = true,
                    IssuerSigningKey = new SymmetricSecurityKey(key),
                    ValidateIssuer = false,
                    ValidateAudience = false,
                    //SaveSigninToken = true,                                
                    ClockSkew = TimeSpan.Zero
                }, out SecurityToken validatedToken);

                var jwtToken = (JwtSecurityToken)validatedToken;
                var userId = jwtToken.Claims.First(x => x.Type == "id").Value;

                // attach user to context on successful jwt validation
                context.Items["User"] = await userService.GetById(userId);
            }
            catch
            {
                // do nothing if jwt validation fails
                // user is not attached to context so request won't have access to secure routes
            }
        }
    }
}
